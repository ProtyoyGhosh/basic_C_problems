// switch case structure
#include <stdio.h>
int main(){
    int i = 2;
    switch (i)
    {
        case 1:
            printf("i am in case 1");
            break;

        case 2:
            printf("i am in case 2");
            break;
        
        case 3:
            printf("i am in case 3");
            break;
        
        case 4:
            printf("i am in case 4");
            break;
        
        default:
        printf("I am in default");
            break;
    }
    return 0;
}